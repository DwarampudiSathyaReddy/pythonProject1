# Addition
num1 = 10
num2 = 5
sum = num1 + num2
print("Sum:", sum)

# Subtraction
difference = num1 - num2
print("Difference:", difference)

# Multiplication
product = num1 * num2
print("Product:", product)

# Division
quotient = num1 / num2
print("Quotient:", quotient)

# Modulus (Remainder)
remainder = num1 % num2
print("Remainder:", remainder)

# Exponentiation
base = 2
exponent = 3
result = base ** exponent
print("Result:", result)
